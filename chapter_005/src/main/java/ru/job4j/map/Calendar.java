package ru.job4j.map;

/**
 * Класс определющий календарный день.
 * @author agavrikov
 * @since 19.07.2017
 * @version 1
 */
public class Calendar {

    /**
     * Дата в строковом представлении.
     */
    String date;

    /**
     * Конструктор.
     * @param date строковое представление даты.
     */
    public Calendar(String date) {
        this.date = date;
    }
}
